        <h2 style="font-size: 35px;">Workout A: Full Body Strength & Power</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;This workout targets major muscle groups and incorporates explosive movements for power development.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Warm-up: 5-10 minutes of light cardio (jogging, jumping jacks) and dynamic stretching (arm circles, leg swings).</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Exercises:</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;1. Barbell Back Squats: 3 sets of 8-12 repetitions. Focus on controlled descent and powerful ascent.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;2. Bench Press (Barbell or Dumbbell): 3 sets of 8-12 repetitions. Proper form is key here.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;3. Pull-ups (or Lat Pulldowns): 3 sets to failure (or 8-12 repetitions with good form on the lat pulldown).</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Encourage progression towards unassisted pull-ups.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;4. Overhead Press (Barbell or Dumbbell): 3 sets of 8-12 repetitions. Maintain a stable core.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;5. Power Cleans (or Jump Squats): 3 sets of 5-8 repetitions. If power cleans are new, start with jump squats to develop explosive leg power.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;6. Plank: 3 sets, holding for 30-60 seconds. Focus on maintaining a straight line from head to heels.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Cool-down: 5-10 minutes of static stretching, holding each stretch for 20-30 seconds (quad stretch, hamstring stretch, chest stretch, etc.).</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Workout B: Upper Body Push & Pull</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;This workout focuses on the pushing and pulling muscles of the upper body.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Warm-up: 5-10 minutes of light cardio and dynamic stretching (shoulder rotations, arm swings).</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Exercises:</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;1. Incline Dumbbell Press: 3 sets of 10-15 repetitions. Targets the upper chest.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;2. Bent-Over Barbell Rows (or Dumbbell Rows): 3 sets of 8-12 repetitions. Focus on squeezing the back muscles.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;3. Dips (or Close-Grip Bench Press): 3 sets to failure (or 8-12 repetitions on the close-grip bench press). Targets triceps and lower chest.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;4. Face Pulls: 3 sets of 15-20 repetitions. Excellent for shoulder health and upper back development.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;5. Lateral Raises: 3 sets of 12-15 repetitions. Targets the lateral deltoids.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;6. Bicep Curls (Barbell or Dumbbell): 3 sets of 10-15 repetitions. Focus on controlled movement.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Cool-down: 5-10 minutes of static stretching (triceps stretch, biceps stretch, shoulder stretch, back stretch).</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Workout C: Lower Body & Core Focus</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;This workout emphasizes leg strength and core stability.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Warm-up: 5-10 minutes of light cardio and dynamic stretching (leg swings, hip circles).</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Exercises:</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;1. Deadlifts (Conventional or Romanian): 1-2 heavy sets of 5 repetitions, followed by 1-2 sets of 8-10 repetitions. Emphasize perfect form and start with lighter weights if new to deadlifts.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;2. Front Squats (or Goblet Squats): 3 sets of 10-15 repetitions. Great for quad and core development.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;3. Walking Lunges: 3 sets of 10-12 repetitions per leg. Focus on controlled movement and knee tracking.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;4. Hamstring Curls (Machine or Bodyweight Glute Bridges): 3 sets of 12-15 repetitions (or 15-20 repetitions for glute bridges).</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;5. Calf Raises (Standing or Seated): 3 sets of 15-20 repetitions.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;6. Hanging Leg Raises (or Crunches): 3 sets of 10-15 repetitions (or 15-20 repetitions for crunches). Focus on controlled movement.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Cool-down: 5-10 minutes of static stretching (quad stretch, hamstring stretch, hip flexor stretch, calf stretch).</h3>
            </li><br>
        </ul><br>

