        <h2 style="font-size: 35px;">Workout:</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;Perform each exercise for the recommended number of repetitions (reps) or time, followed by a short rest (30-60 seconds) before moving to the next exercise. Complete 2-3 rounds of the entire circuit.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;1. Bodyweight Squats:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>How to: Stand with your feet shoulder-width apart, chest up, and core engaged. Lower your hips down as if you're sitting in a chair, keeping your back straight and your knees behind your toes. Return to the starting position.</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 8-12</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;2. Wall Push-ups:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>How to: Stand facing a wall, slightly further than arm's length away. Place your hands</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>shoulder-width apart on the wall. Lean towards the wall, bending your elbows until your chest gets close to the wall. Push back to the starting position. (This is a gentler variation of a standard push-up).</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 8-12</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;3. Walking Lunges:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>How to: Stand with your feet hip-width apart. Step forward with one leg, lowering your hips until both knees are bent at a 90-degree angle. Ensure your front knee stays behind your toes and your back knee hovers just above the ground. Push off with your front foot and step forward with your other leg, repeating the movement.</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 6-10 per leg</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;4. Plank:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>How to: Start in a push-up position, then lower down onto your forearms, keeping your body in a straight line from head to heels. Engage your core and avoid letting your hips sag or rise too high.</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Hold: 30-60 seconds</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;5. Glute Bridges:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>How to: Lie on your back with your knees bent and feet flat on the floor, hip-width apart. Engage your glutes and lift your hips off the ground until your body forms a straight line from your shoulders to your knees. Squeeze your glutes at the top and then slowly lower back down./h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 10-15</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
