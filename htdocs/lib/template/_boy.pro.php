        <h2 style="font-size: 35px;">Foundation Builder :</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;Goal: To introduce basic exercises, improve fundamental movement patterns, and build a base level of strength and endurance.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Frequency: 2-3 non-consecutive days per week (e.g., Monday, Wednesday, Friday). Rest days are crucial for recovery!</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Warm-up (5-10 minutes):</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;Light cardio: Jumping jacks (30 seconds), high knees (30 seconds), butt kicks (30 seconds) - repeat twice.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Dynamic stretching: Arm circles (forward and backward 10 reps each), leg swings (forward and sideways - 10 reps each leg), torso twists (10 reps each side).</h3>
            </li><br>
        </ul><br>
        <h2 style="font-size: 35px;">Workout:</h2><br>
        <ul>
            <li style="margin-left: 105px;">
                <h3>&ensp;Perform each exercise for the specified number of sets and repetitions (reps). Rest for 60-90 seconds between sets.</h3>
            </li><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;1. Bodyweight Squats:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>Sets: 3</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 8-12</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Focus: Keeping the chest up, back straight, and going as low as comfortably possible (ideally thighs parallel to the ground). Imagine sitting back into a chair.</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;2. Push-ups (on knees or toes):</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>Sets: 3</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: As many reps as possible (AMRAP) with good form.</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Focus: Maintaining a straight line from head to heels. If full push-ups are too difficult, start with knee push-ups.</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;3. Walking Lunges:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>Sets: 3</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 10-12 per leg</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Focus: Stepping forward, lowering the back knee towards the ground (without touching), and ensuring the front knee stays behind the toes.</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;4. Plank:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>Sets: 3</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Hold: 30-60 seconds</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Focus: Maintaining a straight line from head to heels, engaging the core muscles (imagine bracing for a punch).</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;5. Glute Bridges:</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>Sets: 3</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Reps: 12-15</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Focus: Lying on the back with knees bent and feet flat, squeezing the glutes to lift the hips off the ground until the body forms a straight line from shoulders to knees.</h5>
                </li>
            </ul><br>
            <li style="margin-left: 105px;">
                <h3>&ensp;Cool-down (5-10 minutes):</h3>
            </li><br>
            <ul>
                <li style="margin-left: 138px;">
                    <h5>Static stretching: Hold each stretch for 20-30 seconds.</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Focus on the major muscle groups worked:</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Quadriceps stretch (standing or lying down)</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Hamstring stretch (seated or standing toe touch)</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Chest stretch (clasp hands behind back and straighten arms)</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Triceps stretch (arm overhead, elbow bent, gently pull elbow with opposite hand)</h5>
                </li>
                <li style="margin-left: 138px;">
                    <h5>Calf stretch (against a wall or step)</h5>
                </li>
            </ul><br>

